words = ["Alpha", "Bravo", "Charlie", "Delta"]
print(f"words {words}")

print(f"words[1:] {words[1:]}")
print(f"words[2:] {words[2:]}")

print(f"words[-1:] {words[-1:]}")
print(f"words[-2:] {words[-2:]}")

print(f"words[:1] {words[:1]}")
print(f"words[:2] {words[:2]}")

print(f"words[:-1] {words[:-1]}")
print(f"words[:-2] {words[:-2]}")

print(f"words[1:-1] {words[:-1]}")
